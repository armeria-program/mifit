void MIGetSpacegroups(std::vector<std::string> &spacegroups)
{
    char name[100];
    int number, nops;
    char buf[128];

    // find this spacegroup in the list and copy the info
    size_t nline = 0;
    while (strncmp(syminfo[nline], "END", 3) != 0)
    {
        if (sscanf(syminfo[nline], "%d%d%*s%s", &number, &nops, name) == 3)
        {
            sprintf(buf, "%d: %s", number, name);
            spacegroups.push_back(buf);
            nline += nops;
        }
        nline++;
    }
}
