int CMapHeaderBase::scan_symmops(const char *buf, float symop[3][4][MISymmop::MAXSYMMOPS], char opstring[MISymmop::MAXSYMMOPS][MISymmop::MAXSTRING])
{
    char a;
    int i, j;
    int isnum = true;
    double num = 0, den = 0, n = 0, sign = 1;
    int ndigit = 0;
    int row = 0, nops = 0;
    for (i = 0; i < 3; i++)
    {
        for (j = 0; j < 4; j++)
        {
            for (unsigned int k = 0; k < MISymmop::MAXSYMMOPS; k++)
            {
                symop[i][j][k] = 0.0;
            }
        }
    }
    for (unsigned int i = 0; i < MISymmop::MAXSYMMOPS; i++)
    {
        strcpy(opstring[i], "");
    }
    j = 0;
    for (i = 0; i < ((int)strlen(buf)); i++)
    {

        /*  added 1/5/99 dem */
        if (isspace(buf[i]))
        {
            continue;
        }

        a = buf[i];
        if (a != '.' && a != ' ' && a != ';' && a != '.')
        {
            opstring[nops][j] = a;
            opstring[nops][j+1] = 0;
            j++;
        }
        if (isdigit(a))
        {
            ndigit++;
        }
        else
        {
            ndigit = 0;
            if (!(a == '/' || a == ' '))
            {
                num = 0;
                den = 0;
            }
        }
        switch (a)
        {
        case 'x':
        case 'X':
            symop[row][0][nops] = (float)sign;
            sign = 1;
            isnum = true;
            break;
        case 'y':
        case 'Y':
            symop[row][1][nops] = (float)sign;
            sign = 1;
            isnum = true;
            break;
        case 'z':
        case 'Z':
            symop[row][2][nops] = (float)sign;
            sign = 1;
            isnum = true;
            break;
        case '-':
            sign = -1;
            isnum = true;
            break;
        case '+':
            sign = 1;
            isnum = true;
            break;
        case ';':
            if (row != 2)
            {
                Logger::debug("error in op %d\n", nops);
            }
            nops++;
            row = 0;
            a = 0;
            j = 0;
            isnum = true;
            break;
        case ',':
            row++;
            sign = 1;
            isnum = true;
            break;
        case '/':
            isnum = false;
            break;
        default:
            if (isdigit(a))
            {
                n = (int)a-(int)'0';
                if (isnum)
                {
                    num = num*10.0+n;
                }
                else
                {
                    den = den*10.0+n;
                }
                if (den != 0)
                {
                    symop[row][3][nops] = (float)(sign*num/den);
                }
            }
            break;
        }
    }
    if (row == 2)
    {
        /*opstring[nops][j-1]=0; */
        opstring[nops][j] = 0;
        nops++;
    }
    return (nops);
}

int CMapHeaderBase::addcenter(int n, float symop[3][4][MISymmop::MAXSYMMOPS])
{
    int i, j, c;
    int nops;

    nops = n;
    if (n > (int)MISymmop::MAXSYMMOPS/2)
    {
        Logger::debug("Addcenter: aborted too many ops\n");
        return (0);
    }
    Logger::log("Adding Patterson inverse center and removing translations");
    for (i = 0; i < n; i++)
    {
        for (j = 0; j < 3; j++)
        {
            for (c = 0; c < 3; c++)
            {
                symop[j][c][nops] = -symop[j][c][i];
            }
            symop[j][3][nops] = 0.0;
            symop[j][3][i] = 0.0;
        }
        nops++;
    }
    return (nops);
}

